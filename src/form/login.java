    private void jToggleButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton2ActionPerformed
        try {
            String sql="Select * from member where username='"+jTextField1.getText()+"' and password='"+jPasswordField1.getText()+"'";
            pst=koneksi.prepareStatement(sql);
            rst=pst.executeQuery();
            if (rst.next()) {
                if(jTextField1.getText().equals(rst.getString("username"))&&jPasswordField1.getText().equals(rst.getString("password")))
                {
                    JOptionPane.showMessageDialog(null, "Selamat datang '"+jTextField1.getText()+"'");
                    new Home2().setVisible(true);
                    dispose();
                }
            } else {JOptionPane.showMessageDialog(null, "Username / password tidak cocok dengan yang terdaftar");
                jTextField1.setText("");
                jPasswordField1.setText("");
            }
        }catch (Exception e) {JOptionPane.showMessageDialog(null, e);}
    }//GEN-LAST:event_jToggleButton2ActionPerformed

